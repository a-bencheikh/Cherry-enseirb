%--------------------------------------------------------------
% rotation autour de l'axe y
%--------------------------------------------------------------
  function T=get_rot_y(theta_rad)
    c=cos(theta_rad); s=sin(theta_rad);
    T=[[ c, 0, s,0]
       [ 0, 1, 0,0]
       [-s, 0, c,0]       
       [ 0, 0, 0,1]       
      ]; 
  end